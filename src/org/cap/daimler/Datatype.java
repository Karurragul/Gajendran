package org.cap.daimler;

public class Datatype {

	public static void main(String[] args) {
		byte age = 26;
		System.out.println("Age is :" + age);
		short empid = 1234;
		System.out.println("Emp Id " + empid);
		int registrationno = 12345678;
		System.out.println("Reg no" + registrationno);
		long mobile = 9999988888l;
		System.out.println("Mobile " + mobile);
		float salary = 65008.85f;
		System.out.println("salary" + salary);
		char batch = 'A';
		System.out.println("batch" + batch);
		String mailid = "abc@gmail.com";
		System.out.println("mail" + mailid);
		boolean isempid = true;
		System.out.println(isempid);
	}
}
